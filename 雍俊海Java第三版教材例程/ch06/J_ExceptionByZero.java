public class J_ExceptionByZero
{
    public static void main(String args[ ])
    {
        int a= 10;
        int b = 0;
        System.out.println("a=" + a);
        System.out.println("b=" + b);
        System.out.println("a/b=" + a/b);
    } // 方法main结束
} // 类J_ExceptionByZero结束


